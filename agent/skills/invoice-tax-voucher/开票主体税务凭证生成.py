def _load_vouchers(invoice_path: Path) -> list[Voucher]:
    wb = load_workbook(invoice_path, data_only=True)
    ws = wb.active

    col: dict[str, int] = {}
    for c in range(1, ws.max_column + 1):
        v = ws.cell(1, c).value
        if v: col[str(v).strip()] = c

    # 检查必要的列是否存在
    required_cols = ["审批编号", "客户/项目名称", "内部类型分类",
                     "开票总额", "差额开票金额", "增值税金额", "不含税金额"]
    missing = [c for c in required_cols if c not in col]
    if missing:
        actual = list(col.keys())
        raise ValueError(
            f"文件缺少必要的列: {missing}\n"
            f"实际列名: {actual}\n"
            f"请确认上传的是正确的【开票主体Excel（金蝶云格式）】文件，而非凭证明细模板。"
        )

    def rc(name: str) -> int:
        return col.get(name, 1)  # 默认返回1而非0，避免 col 不存在时 cell(row,0) 崩溃

    vouchers: list[Voucher] = []
    serial = 40000
    vno = 1

    for r in range(2, ws.max_row + 1):
        审批编号 = ws.cell(r, rc("审批编号")).value
        if not 审批编号: continue
        审批编号 = str(审批编号).strip()
        if 审批编号.startswith("~"): continue

        客户名称 = str(ws.cell(r, rc("客户/项目名称")).value or "").strip()
        内部类型 = str(ws.cell(r, rc("内部类型分类")).value or "").strip()
        开票总额 = round(_to_float(ws.cell(r, rc("开票总额")).value), 2)
        差额开票 = round(_to_float(ws.cell(r, rc("差额开票金额")).value), 2)
        增值税额 = round(_to_float(ws.cell(r, rc("增值税金额")).value), 2)
        不含税   = round(_to_float(ws.cell(r, rc("不含税金额")).value), 2)

        if 开票总额 <= 0: continue

        # 凭证日期
        try:
            凭证日期 = dt.datetime.strptime(审批编号[:8], "%Y%m%d").date()
        except ValueError:
            凭证日期 = dt.date.today()

        # 客户编码/名称
        cust_code, cust_name = _find_customer(客户名称)

        # 摘要：始终使用发票原始客户名称（不用客户字典里的名称，避免带后缀）
        摘要名 = 客户名称
        年月 = 凭证日期.strftime("%Y%m")  # 如 "202511"
        摘要后缀 = f"{年月}月开票"

        # 判断业务类型
        is_派遣 = 内部类型 in ("派遣业务",)
        is_承揽 = 内部类型 in ("承揽业务", "小时间承揽")
        is_代理 = 内部类型 in ("代理业务",)

        if is_代理:
            rev_acct = ACCT_REVENUE代理
        elif is_承揽:
            rev_acct = ACCT_REVENUE承揽
        elif is_派遣:
            rev_acct = ACCT_REVENUE派遣
        else:
            rev_acct = ACCT_REVENUE承揽

        # ── 分录（与输入列一一对应，不反推税率）────────────────────────
        # 借 1122 = 开票总额；贷 6001 = 不含税金额；贷 2221 = 增值税金额；
        # 差额开票金额>0 时再贷一笔 6001 = 差额开票金额。
        # 贷方顺序：收入(不含税) → 销项税额 → 收入(差额)
        lines: list[VLine] = [
            VLine(
                f"{摘要名}{摘要后缀}",
                ACCT_RECEIVABLE, _acct_name(ACCT_RECEIVABLE),
                开票总额, None, 开票总额,
                cust_code, cust_name,
            ),
            VLine(
                f"{摘要名}{摘要后缀}",
                rev_acct, _acct_name(rev_acct),
                None, 不含税, 不含税,
            ),
        ]
        if abs(增值税额) > 0.001:
            lines.append(VLine(
                f"{摘要名}{摘要后缀}",
                ACCT_TAX_SALES, _acct_name(ACCT_TAX_SALES),
                None, 增值税额, 增值税额,
            ))
        if 差额开票 > 0.001:
            lines.append(VLine(
                f"{摘要名}{摘要后缀}",
                rev_acct, _acct_name(rev_acct),
                None, 差额开票, 差额开票,
            ))

        vouchers.append(Voucher(
            date=凭证日期, serial=serial, vno=vno,
            org_name=客户名称, lines=lines,
        ))
        serial += 1; vno += 1

    return vouchers


