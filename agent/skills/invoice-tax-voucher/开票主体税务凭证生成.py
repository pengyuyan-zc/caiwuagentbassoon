def _build_customer_map_from_embedded() -> dict[str, tuple[str, str]]:
    """
    从嵌入的客户数据构建客户映射表
    使用与 _load_customer_map 相同的规范化逻辑
    """
    def _normalize(s: str) -> str:
        return re.sub(r"\s+", "", str(s).strip().replace("（", "(").replace("）", ")"))

    def _strip_suffix(s: str) -> str:
        for suf in ("有限公司", "有限责任公司", "股份有限公司"):
            if s.endswith(suf):
                return s[:-len(suf)]
        return s

    result: dict[str, tuple[str, str]] = {}

    for code, name, short in EMBEDDED_CUSTOMER_DATA:
        # 过滤禁用客户（已在导出时过滤）
        # 添加规范化名称
        result[_normalize(name)] = (code, name)
        # 添加去掉后缀的名称
        stripped = _strip_suffix(name)
        if stripped:
            result[_normalize(stripped)] = (code, name)
        # 添加简称
        if short:
            result[_normalize(short)] = (code, name)

    return result


def _load_customer_map(path: Path) -> dict[str, tuple[str, str]]:
    wb = load_workbook(path, data_only=True)
    ws = wb.active

    col: dict[str, int] = {}
    for c in range(1, ws.max_column + 1):
        v = ws.cell(1, c).value
        if v: col[str(v).strip()] = c

    code_col = col.get("客户编码", 0)
    name_col = col.get("客户名称", col.get("客户", 0))
    short_col = col.get("简称", 0)

    result: dict[str, tuple[str, str]] = {}

    def _strip_suffix(s: str) -> str:
        for suf in ("有限公司", "有限责任公司", "股份有限公司"):
            if s.endswith(suf): return s[:-len(suf)]
        return s

    for r in range(2, ws.max_row + 1):
        code = ws.cell(r, code_col).value
        name = ws.cell(r, name_col).value
        if not code or not name: continue
        code_s = str(code).strip()
        name_s = str(name).strip()
        # 过滤名称中带"（禁用）"的客户（双重保险：名称前缀和规范化后前缀都检查）
        if name_s.startswith("（禁用）"): continue
        name_n = _normalize(name_s)
        if name_n.startswith("(") and name_n.lstrip("(").startswith("\u7981"): continue

        result[_normalize(name_s)] = (code_s, name_s)
        short = _strip_suffix(name_s)
        if short:
            result[_normalize(short)] = (code_s, name_s)
        if short_col:
            sv = ws.cell(r, short_col).value
            if sv:
                sv_s = str(sv).strip()
                if sv_s:
                    result[_normalize(sv_s)] = (code_s, name_s)

    return result



def _normalize(s: str) -> str:
    return re.sub(r"\s+", "", s.strip().replace("（", "(").replace("）", ")"))


