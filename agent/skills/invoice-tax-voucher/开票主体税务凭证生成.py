def _reorder_amount_columns(ws, orig, drc, crc):
    """将原币/借方/贷方三列调整为正确顺序：原币→借方→贷方"""
    if not (orig and drc and crc):
        return
    # 收集所有行（含列头行）的值
    orig_vals = [ws.cell(r, orig).value for r in range(1, ws.max_row + 1)]
    drc_vals  = [ws.cell(r, drc).value  for r in range(1, ws.max_row + 1)]
    crc_vals  = [ws.cell(r, crc).value  for r in range(1, ws.max_row + 1)]
    # 目标：最小的列放原币，中间的放借方，最大的放贷方
    col1, col2, col3 = sorted([orig, drc, crc])
    for r in range(1, ws.max_row + 1):
        ws.cell(r, col1).value = orig_vals[r - 1]  # 原币
        ws.cell(r, col2).value = drc_vals[r - 1]   # 借方
        ws.cell(r, col3).value = crc_vals[r - 1]   # 贷方


def _write_vouchers(
    模板_path: Path,
    输出_path: Path,
    vouchers: list[Voucher],
    book_code: str,
    group_code: str,
    fentity_start: int,
) -> None:
    shutil.copy2(模板_path, 输出_path)
    wb = load_workbook(输出_path)
    ws = wb.worksheets[0]

    col = _build_col_map(ws)

    def g(name: str) -> int | None:
        return col.get(name)

    sc   = g("*单据头(序号)");          bc   = g("*(单据头)账簿#编码")
    bnc  = g("(单据头)账簿#名称");       onc  = g("(单据头)核算组织#名称")
    dc   = g("*(单据头)日期");           yc   = g("(单据头)会计年度")
    pc   = g("(单据头)期间");            gc2  = g("*(单据头)凭证字#编码")
    nc   = g("*(单据头)凭证号");         fec  = g("*分录(序号)")
    suc  = g("(分录)摘要");             ac2  = g("*(分录)科目编码#编码")
    anc  = g("(分录)科目编码#名称");    ccc  = g("(分录)客户#编码")
    cnc  = g("(分录)客户#名称(Null)");ccuc = g("*(分录)币别#编码")
    cnuc = g("(分录)币别#名称");       etc  = g("*(分录)汇率类型#编码")
    etcn = g("(分录)汇率类型#名称");    drc  = g("(分录)借方金额")
    crc  = g("(分录)贷方金额");        orig = g("(分录)原币金额")

    # 删除旧数据行
    rows_del = ws.max_row - 2
    if rows_del > 0:
        ws.delete_rows(3, rows_del)

    out_row = 3
    fentry  = fentity_start
    book_name = "广东锐博人力资源服务有限公司"

    for v in vouchers:
        for line in v.lines:
            if sc:   ws.cell(out_row, sc).value   = v.serial
            if bc:   ws.cell(out_row, bc).value   = book_code
            if bnc:  ws.cell(out_row, bnc).value  = book_name
            if onc:  ws.cell(out_row, onc).value  = book_name
            if dc:   ws.cell(out_row, dc).value   = str(v.date)
            if yc:   ws.cell(out_row, yc).value   = v.date.year
            if pc:   ws.cell(out_row, pc).value   = v.date.month
            if gc2:  ws.cell(out_row, gc2).value  = group_code
            if nc:   ws.cell(out_row, nc).value   = v.voucher_no
            if fec:
                ws.cell(out_row, fec).value = fentry
                fentry += 1
            if suc:  ws.cell(out_row, suc).value  = line.summary
            if ac2:  ws.cell(out_row, ac2).value  = line.account_code
            if anc:  ws.cell(out_row, anc).value  = line.account_name
            if ccc:  ws.cell(out_row, ccc).value  = line.cust_code
            if cnc:  ws.cell(out_row, cnc).value  = line.cust_name
            if ccuc: ws.cell(out_row, ccuc).value = "PRE001"
            if cnuc: ws.cell(out_row, cnuc).value = "人民币"
            if etc:  ws.cell(out_row, etc).value  = "HLTX01_SYS"
            if etcn: ws.cell(out_row, etcn).value = "固定汇率"
            # 先写入借/贷/原币（用临时变量保存）
            orig_val = round(line.orig_amount, 2) if line.orig_amount else None
            drc_val  = round(line.debit, 2)      if line.debit      else None
            crc_val  = round(line.credit, 2)     if line.credit     else None
            # 按目标顺序写入：原币 → 借方 → 贷方
            if orig: ws.cell(out_row, orig).value = orig_val
            if drc:  ws.cell(out_row, drc).value  = drc_val
            if crc:  ws.cell(out_row, crc).value  = crc_val
            out_row += 1

    # 调整列物理顺序：原币 → 借方 → 贷方
    _reorder_amount_columns(ws, orig, drc, crc)

    wb.save(输出_path)


